.segment "ZEROPAGE"
Frame:      .res 1
Clock60:    .res 1
BgPtr:      .res 2      ; store the address to the background data, to store an address we first 
                        ; store the lo-byte then the hi-byte

.segment "CODE"

; TODO move to a new file?
.macro ppu_set_address addr
    bit PPU_STATUS
    lda #>addr
    sta PPU_ADDR 
    lda #<addr 
    sta PPU_ADDR
.endmacro


.proc loadPalette 
    
    ppu_set_address $3F00

    ldy #0
:   lda PaletteData,y 
    sta PPU_DATA
    iny
    cpy #32
    bne :-
    rts
.endproc

.proc loadBackground 
    lda #<BackgroundData
    sta BgPtr
    lda #>BackgroundData
    sta BgPtr + 1

    ppu_set_address $2000 

    ldy #0
    ldx #0

OutterLoop:
InnerLoop:
    lda (BgPtr),y 
    sta PPU_DATA
    iny
    cpy #0
    beq IncreaseHiByte
    jmp InnerLoop
IncreaseHiByte:
    inc BgPtr+1
    inx
    cpx #4
    bne OutterLoop
    rts
.endproc

.proc loadAttributes 
    ldy #0
:   lda AttributeData,y 
    sta PPU_DATA
    iny
    cpy #16
    bne :-
    rts
.endproc


RESET:
    init_nes

    lda #0
    sta Frame
    sta Clock60

Main:

    jsr loadPalette         ; jump to subroutine

    jsr loadBackground

    lda #%10010000           ; Enable NMI and set background to use the 2nd pattern table (at $1000)
    sta PPU_CTRL
    lda #0
    sta PPU_SCROLL
    sta PPU_SCROLL
    lda #%00011110
    sta PPU_MASK


LoopForever:
    jmp LoopForever


NMI:
    inc Frame
    lda Frame
    cmp #60
    bne :+
    inc Clock60
    lda #0
    sta Frame
:
    ; TODO increment clock60 everytime Frame reaches (reset Frame at this point as well)
    rti

IRQ:
    rti


